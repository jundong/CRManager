ValidationResultsViewModel = function (configurationVm) {
    var self = this;

    if (configurationVm) {
        // configurationVm not used for basic functionality
        self.configurationVm = configurationVm;
        self.rootVm = configurationVm.rootVm;
    }

    self.is_valid = true;
    self.is_ready = false;
    self.ignore_warning = false;
    self.messages = new Array();
    self.warnings = new Array();
    self.id = -1;
};

ValidationResultsViewModel.typesafe = function (that) {
    if (!(that instanceof ValidationResultsViewModel))
        throw 'This method must be executed on a ValidationResultsViewModel';
    return that;
};

