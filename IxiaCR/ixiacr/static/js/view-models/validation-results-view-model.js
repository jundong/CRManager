ValidationResultsViewModel.prototype.inflate = function(validationData){
    var self = ValidationResultsViewModel.typesafe(this);
    self.is_valid = validationData.is_valid;
    self.is_ready = validationData.is_ready || false;
    self.message = validationData.message;
    self.messages = validationData.messages || [];

    if (validationData.items && validationData.items.length > 0 && validationData.items[0].id !== null && validationData.items[0].id !== undefined) {
        self.id = validationData.items[0].id;
    }

    for(var i = 0; i < self.messages.length; i++){
        var reason = self.messages[i].content;
        if(reason && (typeof reason === "string")) reason = [reason];// convert reason to an array
        self.messages[i].content = reason;
    }
};

ValidationResultsViewModel.prototype.addCheckResults = function(name, success, reason){
    var self = ValidationResultsViewModel.typesafe(this);

    if(name == null)
        throw 'ValidationResultsViewModel.addCheckResults expects a name parameter';

    if(success == null)
        success = true;

    if(!success)
        self.is_valid = false;

    var check = ko.utils.arrayFirst(self.messages, function(item) { return item.header == name; });
    if(!check){
        if(reason && (typeof reason === "string")) reason = [reason];// convert reason to an array
        check = {
            header: name,
            content: reason,
            is_error: !success
        };
        self.messages.push(check);
    }
    else{
        check.content.push(reason);
    }
};
ValidationResultsViewModel.prototype.addCheckWarnings = function(name, reason){
    var self = ValidationResultsViewModel.typesafe(this);

    if(name == null)
        throw 'ValidationResultsViewModel.addCheckWarnings expects a name parameter';

    var check = ko.utils.arrayFirst(self.warnings, function(item) { return item.header == name; });
    if(!check){
        if(reason && (typeof reason === "string")) reason = [reason];// convert reason to an array
        check = {
            header: name,
            content: reason
        };
        self.warnings.push(check);
    }
    else{
        check.content.push(reason);
    }
};
